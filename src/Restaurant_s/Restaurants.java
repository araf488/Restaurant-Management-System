    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            Document doc = new Document();
            try {
                PdfWriter.getInstance(doc, new FileOutputStream("receipt.pdf"));
            } catch (DocumentException ex) {
                Logger.getLogger(Restaurants.class.getName()).log(Level.SEVERE, null, ex);
            }
            doc.open();
            try {
                doc.add(new Paragraph(jTextArea1.getText()));
                JOptionPane.showMessageDialog(null, "File Save");
            } catch (DocumentException ex) {
                Logger.getLogger(Restaurants.class.getName()).log(Level.SEVERE, null, ex);
            }
            doc.close();

        } catch (FileNotFoundException ex) {
            Logger.getLogger(Restaurants.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

